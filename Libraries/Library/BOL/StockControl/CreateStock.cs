using System;
using System.Collections.Generic;
using System.Text;

using Library.BOL.Products;

namespace Library.BOL.StockControl
{
    [Serializable]
    public sealed class CreateStock : BaseCollection
    {
        #region Public Methods

        /// <summary>
        /// Creates a new link for this ProductCost item to another item
        /// </summary>
        /// <param name="primary"></param>
        /// <param name="subItem"></param>
        /// <param name="quantity"></param>
        /// <returns></returns>
        public CreateStockItem Add(ProductCost primary, ProductCost subItem, int quantity)
        {
            CreateStockItem Result = null;
            
            Result = DAL.FirebirdDB.StockCreateItemsAdd(primary, subItem, quantity);

            if (Result != null)
                this.Add(Result);

            return (Result);
        }

        /// <summary>
        /// Updates the quanity for a product dependency
        /// </summary>
        /// <param name="primary">Primary Item</param>
        /// <param name="subItem">Sub Item</param>
        /// <param name="quantity">new Quantity</param>
        public void Update(ProductCost primary, ProductCost subItem, double quantity)
        {
            DAL.FirebirdDB.StockCreateItemsUpdate(primary, subItem, quantity);
        }

        /// <summary>
        /// Delete's a product creation dependency
        /// </summary>
        /// <param name="primary">Primary ProductCost</param>
        /// <param name="subItem">Sub ProductCost being removed</param>
        public void Delete(ProductCost primary, ProductCost subItem)
        {
            DAL.FirebirdDB.StockCreateItemsDelete(primary, subItem);
        }

        #endregion Public Methods

        #region Generic CollectionBase Code

        #region Properties

        /// <summary>
        /// Indexer Property
        /// </summary>
        /// <param name="Index">Index of object to return</param>
        /// <returns>CreateStockItem object</returns>
        public CreateStockItem this[int Index]
        {
            get
            {
                return ((CreateStockItem)this.InnerList[Index]);
            }

            set
            {
                this.InnerList[Index] = value;
            }
        }

        #endregion Properties

        #region Public Methods

        /// <summary>
        /// Adds an item to the collection
        /// </summary>
        /// <param name="value"></param>
        /// <returns></returns>
        public int Add(CreateStockItem value)
        {
            return (List.Add(value));
        }

        /// <summary>
        /// Returns the index of an item within the collection
        /// </summary>
        /// <param name="value"></param>
        /// <returns></returns>
        public int IndexOf(CreateStockItem value)
        {
            return (List.IndexOf(value));
        }

        /// <summary>
        /// Inserts an item into the collection
        /// </summary>
        /// <param name="index"></param>
        /// <param name="value"></param>
        public void Insert(int index, CreateStockItem value)
        {
            List.Insert(index, value);
        }


        /// <summary>
        /// Removes an item from the collection
        /// </summary>
        /// <param name="value"></param>
        public void Remove(CreateStockItem value)
        {
            List.Remove(value);
        }


        /// <summary>
        /// Indicates the existence of an item within the collection
        /// </summary>
        /// <param name="value"></param>
        /// <returns></returns>
        public bool Contains(CreateStockItem value)
        {
            // If value is not of type OBJECT_TYPE, this will return false.
            return (List.Contains(value));
        }

        #endregion Public Methods

        #region Private Members

        private const string OBJECT_TYPE = "Library.BOL.StockControl.CreateStockItem";
        private const string OBJECT_TYPE_ERROR = "Must be of type CreateStockItem";


        #endregion Private Members

        #region Overridden Methods

        /// <summary>
        /// When Inserting an Item
        /// </summary>
        /// <param name="index"></param>
        /// <param name="value"></param>
        protected override void OnInsert(int index, Object value)
        {
            if (value.GetType() != Type.GetType(OBJECT_TYPE))
                throw new ArgumentException(OBJECT_TYPE_ERROR, "value");
        }


